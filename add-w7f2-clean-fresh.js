function parseW7F2Data(rawText) {
  const voters = [];
  const lines = rawText.trim().split('\n');
  
  let currentVoter = {};
  
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i].trim();
    
    if (!line) continue;
    
    // Check if line starts with a serial number
    if (/^\d+\s+[A-Z]{3}\d+/.test(line)) {
      // Save previous voter if exists
      if (currentVoter.serial) {
        voters.push({...currentVoter});
      }
      
      // Parse new voter header
      const parts = line.split(/\s+/);
      currentVoter = {
        serial: parseInt(parts[0]),
        voterId: parts[1],
        uniqueId: parts[2] || ''
      };
    }
    else if (line.startsWith('मतदाराचे पूर्ण:') || line.startsWith('मतदाराचे नाव:')) {
      currentVoter.name = line.split(':')[1]?.trim() || '';
    }
    else if (line.startsWith('वडिलांचे नाव:')) {
      currentVoter.father = line.split(':')[1]?.trim() || '';
      currentVoter.relationshipType = 'father';
    }
    else if (line.startsWith('पतीचे नाव:')) {
      currentVoter.husband = line.split(':')[1]?.trim() || '';
      currentVoter.relationshipType = 'husband';
    }
    else if (line.startsWith('इतर:')) {
      currentVoter.other = line.split(':')[1]?.trim() || '';
      currentVoter.relationshipType = 'other';
    }
    else if (line.startsWith('घर क्रमांक:')) {
      currentVoter.houseNumber = line.split(':')[1]?.trim() || 'NA';
    }
    else if (line.startsWith('वय:')) {
      const ageParts = line.split('लिंग:');
      const ageStr = ageParts[0].replace('वय:', '').trim();
      currentVoter.age = parseInt(ageStr) || 0;
      
      const genderStr = ageParts[1]?.trim() || '';
      currentVoter.gender = genderStr === 'पु' ? 'M' : genderStr === 'स्री' ? 'F' : '';
    }
  }
  
  // Add last voter
  if (currentVoter.serial) {
    voters.push({...currentVoter});
  }
  
  return voters;
}

