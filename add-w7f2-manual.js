function parseW7F2ManualData(rawText) {
  const lines = rawText.split('\n').map(line => line.trim()).filter(line => line);
  const voters = [];
  let currentSerials = [];

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];

    // Check if line starts with serial numbers and XUA/CRM
    const serialMatches = line.match(/(\d+)\s+(?:XUA|CRM)\d+/g);
    if (serialMatches) {
      currentSerials = serialMatches.map(match => match.split(' ')[0]);
      continue;
    }

    if (line.includes('मतदाराचे पूर्ण')) {
      const matches = [...line.matchAll(/मतदाराचे पूर्ण\s*[:;]?\s*(.+?)(?:\s*नांव|\s*वडिलांचे नाव|\s*पतीचे नाव|$)/g)];
      const names = matches.map(m => m[1].trim().replace(/नांव/g, '').replace(/वडिलांचे नाव/g, '').replace(/पतीचे नाव/g, '').replace(/घर क्रमांक/g, '').trim());

      for (let i = 0; i < currentSerials.length; i++) {
        const serial = currentSerials[i];
        const name = names[i] || 'Not Available';

        const voter = {
          serial: serial,
          voterId: '',
          name: name,
          ward: '7',
          booth: '2',
          actualWard: '7',
          actualBooth: '2',
          uniqueSerial: 'W7B2S' + serial.padStart(3, '0'),
          serialNumber: serial,
          anukramank: parseInt(serial),
          father: '',
          husband: '',
          houseNumber: '',
          age: '',
          gender: ''
        };

        // Find XUA in nearby lines
        for (let j = Math.max(0, i - 5); j < Math.min(lines.length, i + 5); j++) {
          if (j !== i) {
            const xuaMatch = lines[j].match(/(XUA|CRM)\d+/);
            if (xuaMatch && !voters.some(v => v.voterId === xuaMatch[0])) {
              voter.voterId = xuaMatch[0];
              break;
            }
          }
        }

        voters.push(voter);
      }

      currentSerials = []; // Reset for next group
    }

    // Parse details for last voter
    if (voters.length > 0) {
      const lastVoter = voters[voters.length - 1];

      if (line.includes('वडिलांचे नाव') && !lastVoter.father) {
        const fatherMatch = line.match(/वडिलांचे नाव\s*:\s*(.+)/);
        if (fatherMatch) lastVoter.father = fatherMatch[1].trim();
      }

      if (line.includes('पतीचे नाव') && !lastVoter.husband) {
        const husbandMatch = line.match(/पतीचे नाव\s*:\s*(.+)/);
        if (husbandMatch) lastVoter.husband = husbandMatch[1].trim();
      }

      if (line.includes('घर क्रमांक') && !lastVoter.houseNumber) {
        const houseMatch = line.match(/घर क्रमांक\s*:\s*(.+)/);
        if (houseMatch) lastVoter.houseNumber = houseMatch[1].trim();
      }

      if (line.includes('वय') && line.includes('लिंग') && !lastVoter.age) {
        const ageGenderMatch = line.match(/वय\s*:\s*(\d+)\s*लिंग\s*:\s*(.+)/);
        if (ageGenderMatch) {
          lastVoter.age = parseInt(ageGenderMatch[1]);
          const gender = ageGenderMatch[2].trim();
          if (gender === 'पु' || gender === 'पुरुष') {
            lastVoter.gender = 'M';
          } else if (gender === 'स्त्री' || gender === 'स्री') {
            lastVoter.gender = 'F';
          } else {
            lastVoter.gender = gender;
          }
        }
      }
    }
  }

  return voters;
}

